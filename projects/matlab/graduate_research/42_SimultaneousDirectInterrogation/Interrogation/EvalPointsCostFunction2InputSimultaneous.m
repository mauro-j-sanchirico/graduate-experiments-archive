function cost_vector = EvalPointsCostFunction2InputSimultaneous( ...
    active_input_a, active_input_b, mlp_param_vector, ...
    ti_table, tc_table, alpha_coefs, ...
    x_stimuli, y_measured)

mlp_params = ConvertVectorToMLPParams(mlp_param_vector);

y_response = EvalInterrogationPolynomial2InputSimultaneous( ...
    active_input_a, active_input_b, mlp_params, alpha_coefs, ...
    ti_table, tc_table, x_stimuli);

cost_vector = y_response(:) - y_measured(:);

end
