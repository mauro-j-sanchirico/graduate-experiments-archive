clc; clear; close all;

% Compute a groebner basis
groebner( ...
    {'x^2+2*x*y^2', ...
     'x*y+2*y^3-1'}, ...
    'lex',{'x','y'})

% Solve a polynomial system
polynsolve( ...
    {'x^2+2*x*y^2', ...
     'x*y+2*y^3-1'}, ...
    '',{'x','y'})

% Solve a trivial polynomial equation
polynsolve( ...
    {'x^2-x', ...
     'x*y-1'}, ...
    '',{'x','y'})

% Solve a system with inifinite solutions (represented by NaN)
polynsolve( ...
    {'x^2-x', ...
     'x*y'}, ...
    '',{'x','y'})
