function [gamma_list, y_exponential_fourier] = ...
    GetExponentialFourierOutputOverGammaList( ...
        x_interrogation, gamma_list)

design_flags = GetDesignFlags();
    
y_exponential_fourier = ...
    zeros(length(x_interrogation), length(gamma_list));

for gamma_idx = 1:length(gamma_list)
    
    gamma_hidden = gamma_list(gamma_idx);
    y_output = GetExponentialFourierOutputY( ...
        gamma_hidden, ...
        design_flags.number_of_harmonics_N, ...
        design_flags.number_of_exponential_terms_M, ...
        design_flags.number_of_taylor_terms_L, ...
        design_flags.psi);
    y_exponential_fourier(:, gamma_idx) = y_output;
    
end
