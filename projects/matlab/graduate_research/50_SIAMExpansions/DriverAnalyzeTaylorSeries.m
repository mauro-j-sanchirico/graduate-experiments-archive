%% Analyzes the Taylor Series for a Class of Solitons
%% Analyzes Taylor Series coefficients and convergence

clc; clear; close all;

set(groot, 'defaulttextInterpreter', 'latex');
set(groot, 'DefaultLegendInterpreter', 'latex');
set(groot, 'DefaultLineLineWidth', 2);
set(groot, 'defaultAxesFontSize', 18);

addpath('Math/TaylorSeriesSech');
addpath('Math/TaylorSeriesSech2');
addpath('Math/TaylorSeriesTanh');
addpath('Math/TaylorSeriesTanh2');
addpath('Analysis');


%% Analyze the hyperbolic secant

big_n = 9;
r = pi/2;

PlotPartialSumComparison( ...
    @sech, @ComputeTaylorNthPartialSumSech, ...
    big_n, r, ...
    '$$x$$', ...
    'True and approx. $$\mathrm{sech}(x)$$');

big_n_max = 40;
r = pi;

PlotConvergenceSurface( ...
    @sech, @ComputeTaylorNthPartialSumSech, ...
    big_n_max, r, ...
    '$$x$$', ...
    'Error in Taylor approx. of $$\mathrm{sech}(x)$$');

big_n_max = 20;

PlotCoefs( ...
    @ComputeTaylorNthCoefSech, big_n_max, ...
    'Taylor coefs. for $$\mathrm{sech}(x)$$');


