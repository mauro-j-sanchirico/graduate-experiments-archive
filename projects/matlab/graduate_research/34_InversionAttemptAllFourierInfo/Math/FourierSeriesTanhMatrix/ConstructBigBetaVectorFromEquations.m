function [big_beta_vector_from_equations, ...
          big_beta_a0_vector_from_equations] = ...
    ConstructBigBetaVectorFromEquations( ...
        number_partial_sum_terms_ns, ...
        gamma10_layer0, gamma11_layer0, gamma11_layer1, alpha_coefs)

k = 0:(2*number_partial_sum_terms_ns - 1);

big_beta_vector_from_equations = zeros(number_partial_sum_terms_ns, 1);

for k_idx = 1:length(k)

    big_beta_vector_from_equations(k_idx) = ...
        ComputeBigBeta( ...
            number_partial_sum_terms_ns, k(k_idx), ...
            gamma10_layer0, gamma11_layer0, gamma11_layer1, alpha_coefs);

end

big_beta_a0_vector_from_equations = ...
    big_beta_vector_from_equations(1:2:end);
